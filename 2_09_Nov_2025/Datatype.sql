Datatypes :
	Datatype is use to restrict a user to enter a data into particular format or type.

	-- Types of datatypes :
		-- String
			String is combination of alphabet, numeric and special char.
			String values are always written with single quote.

			Ex :
				string1 = 'Weltec123@'
				string2 = 'weltec123'
				string3 = 'weltec@'
				string4 = '@123'
				string5 = '123'
				string6 = '@'
				string7 = 'Weltec'

