Truncate :
	Remove / delete data from table.
	Truncate is use when we need to reset constraints in table. Like PK and 
		Increment key

	-- Syntax :
		Truncate table tbl_name

	-- Example :
		Select * from tbl_StudentDetail
		Select * from tbl_StudentMaster

		Insert into tbl_StudentDetail(Student_Id,StudentDetail_Marks,StudentDetail_Subject)
		values(3,25,'SS')

		Insert into tbl_StudentDetail(Student_Id,StudentDetail_Marks,StudentDetail_Subject)
		values(3,56,'Maths')

		Delete from tbl_StudentDetail

		 truncate table tbl_StudentDetail




